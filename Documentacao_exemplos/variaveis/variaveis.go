package main

import "fmt"

func main() {

	fmt.Println("go" + "lang")

	fmt.Println("2+2 =", 4+1)
	fmt.Println("8.2/2.8 =", 8.2/2.8)

	fmt.Println(true && false)
	fmt.Println(true || false)
	fmt.Println(!true)

	var a = "initial"
	fmt.Println(a)

	var b, c int = 8, 22
	fmt.Println(b, c)

	var d = true
	fmt.Println(d)

	var e int
	fmt.Println(e)

	f := "Raphael"
	fmt.Println(f)

}
